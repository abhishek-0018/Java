public class Evaluate_expression {
    public static void main(String[] args) {
        System.out.println("Output for (-5 + 8 * 6) will be = "+(-5 + 8 * 6));
        System.out.println("Output for ((55+9) % 9) will be = "+((55+9) % 9));
        System.out.println("Output for (20 + -3*5 / 8) will be = "+(20 + -3*5 / 8));
        System.out.println("Output for (5 + 15 / 3 * 2 - 8 % 3) will be = "+(5 + 15 / 3 * 2 - 8 % 3));
    }
}
